    def predict_supply_constraint(self, component: Component,
                                    demand_platforms: list[Platform],
                                    current_capacity: int) -> Optional[Prediction]:
        """
        Predict when aggregate Blue UAS demand for a component
        will exceed available supply.
        
        Uses platform production rates + growth trajectories
        to forecast demand curve, compared against known capacity.
        """
        total_monthly_demand = sum(
            p.monthly_production for p in demand_platforms
        )
        
        # Simple linear extrapolation (production would use Prophet/XGBoost)
        avg_growth = sum(p.yoy_growth_pct for p in demand_platforms) / max(len(demand_platforms), 1)
        months_to_constraint = 0
        
        if total_monthly_demand >= current_capacity:
            months_to_constraint = 0  # Already constrained
        elif avg_growth > 0:
            monthly_growth_rate = avg_growth / 100 / 12
            projected = total_monthly_demand
            for m in range(1, 25):
                projected *= (1 + monthly_growth_rate)
                if projected >= current_capacity:
                    months_to_constraint = m
                    break
        
        if months_to_constraint <= 12:
            quarter = f"Q{((datetime.now().month - 1 + months_to_constraint) // 3) % 4 + 1} {datetime.now().year + (datetime.now().month + months_to_constraint - 1) // 12}"
            return Prediction(
                timeframe=quarter,
                event=f"{component.name} allocation-only for defense",
                probability=max(0.4, min(0.95, 0.9 - months_to_constraint * 0.05)),
                impact="critical" if months_to_constraint <= 3 else "high",
                drivers=[
                    f"{len(demand_platforms)} Blue UAS platforms scaling",
                    f"Aggregate demand: {total_monthly_demand}/mo",
                    f"Avg growth: +{avg_growth:.0f}% YoY",
                ],
                model="linear_extrapolation",
                last_updated=datetime.utcnow().isoformat() + "Z",
            )
        
        return None


